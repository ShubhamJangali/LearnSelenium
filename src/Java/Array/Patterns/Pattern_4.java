package Java.Array.Patterns;

public class Pattern_4 {
    public static void main(String[] args) {
        for(int i=1;i<=9;i++){
            if (i%3==0){
                System.out.print(i);
                System.out.println(" ");
            }
            else {
                System.out.print(i);
            }
        }
    }
}

